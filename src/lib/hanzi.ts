// Build lookup map at module load time (skip identity mappings)
const MAP: Record<string, string> = {};
for (const [trad, simp] of Object.entries(T2S)) {
  if (trad !== simp) MAP[trad] = simp;
}

/**
 * Convert a string from traditional to simplified Chinese, character by
 * character.  Non-CJK and already-simplified characters pass through as-is.
 */
export function toSimplified(s: string): string {
  if (!s) return s;
  let out = "";
  for (const ch of s) {
    out += MAP[ch] ?? ch;
  }
  return out;
}
